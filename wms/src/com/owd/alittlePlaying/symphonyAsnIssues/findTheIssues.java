    public static void processTheList(List<clientItem> items){

        List<clientItem> issueList = new ArrayList<clientItem>();
        List<clientItem> errorList = new ArrayList<clientItem>();


        for(clientItem item:items){
            try {
                System.out.println("Doing "+item.getSku());
                OwdInventory inv = InventoryManager.getOwdInventoryForClientAndSku("489", item.getSku());
            String sql = "SELECT\n" +
                    "    dbo.receive.asn_fkey,\n" +
                    "    dbo.receive_item.qty_receive,\n" +
                    "    dbo.receive.post_date," +
                    "dbo.asn.client_ref\n" +
                    "FROM\n" +
                    "    dbo.receive\n" +
                    "LEFT OUTER JOIN\n" +
                    "    dbo.receive_item\n" +
                    "ON\n" +
                    "    (\n" +
                    "        dbo.receive.id = dbo.receive_item.receive_fkey)\n" +
                    "LEFT OUTER JOIN\n" +
                    "    dbo.asn\n" +
                    "ON\n" +
                    "    (\n" +
                    "        dbo.receive.asn_fkey = dbo.asn.id)\n" +
                    "WHERE"+
                    "    dbo.receive.is_posted = 1\n" +
                    "AND dbo.receive_item.inventory_fkey = :invId ;";

                Query q = HibernateSession.currentSession().createSQLQuery(sql);
                q.setParameter("invId",inv.getInventoryId());

                List l = q.list();
                if(l.size()>0){
                    List<owdInfo> owdList = new ArrayList<owdInfo>();
                   int total = 0;
                    boolean fullmatch = false;
                    for(Object row: l){
                        Object[] data = (Object[]) row;
                        owdInfo o = new owdInfo();
                        o.setAsnId(data[0].toString());
                        o.setQty(Integer.parseInt(data[1].toString()));
                        o.setPostDate(data[2].toString());
                        o.setClientRef(data[3].toString());
                        total += o.getQty();
                        if(o.getQty()>0) {
                            owdList.add(o);
                        }
                        if(o.getClientRef().equalsIgnoreCase(item.clientASN)&&o.getQty()==item.getClientTotal()){
                            fullmatch = true;
                        }
                    }
                    if(!fullmatch) {


                        if (total != item.getClientTotal()) {
                            System.out.println("We have a mismatch");
                            item.setReceivedList(owdList);
                            issueList.add(item);
                        } else {
                            System.out.println("We have a match");
                        }
                    }


                }else{
                    System.out.println("No receive");
                }


            }catch (Exception e){
                errorList.add(item);
            }

        }

        if(issueList.size()>0){
            System.out.println("Writing file");
            File f = new File("issues.csv");
            System.out.println(f.getAbsolutePath());
            BufferedWriter writer = null;
            try{
                writer = new BufferedWriter(new FileWriter(f));
                writer.append("symphony id,sku,expected,Symphony received,OWD ASN ID,received,posted date\r\n");
                for(clientItem item:issueList){
                    writer.append(item.clientASN+","+item.getSku()+","+item.getExpected()+","+item.getClientTotal());
                    int i = 0;
                    for(owdInfo owd:item.getReceivedList()){
                        if(i==0){
                            writer.append(","+owd.getAsnId()+","+owd.getQty()+","+owd.getPostDate()+"\r\n");
                        }else{
                            writer.append(",,,,"+owd.getAsnId()+","+owd.getQty()+","+owd.getPostDate()+"\r\n");
                        }
                        i++;
                    }
                }
                writer.flush();
                writer.close();

            }catch (Exception e){
                e.printStackTrace();
            }
        }



    }
